    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String tenSach = jTextField1.getText();
        String tacGia = jTextField2.getText();
        String gia = jTextField3.getText();
        String nam = jTextField4.getText();

        if (!tenSach.isEmpty() && !tacGia.isEmpty() && !gia.isEmpty() && !nam.isEmpty()) {
            tableModel.insertRow(0,new Object[]{tenSach, tacGia, gia, nam});
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable2.getSelectedRow();
        if (selectedRow >= 0) {
            tableModel.removeRow(selectedRow);
    }//GEN-LAST:event_jButton2ActionPerformed
    }

