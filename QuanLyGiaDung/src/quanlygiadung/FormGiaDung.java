    private void loadData(String query) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        ResultSet rs = KetNoi.executeQuery(query);
        try {
            while (rs.next()) {
                Object[] rowData = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)};
                model.addRow(rowData);
            }
        } catch (SQLException ex) {
            JOptionPane.showConfirmDialog(null, "Lỗi: " + ex.getMessage(), "Thông báo", JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
        }
    }
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        loadData("SELECT mahang, tenhang, hangsx, dongia FROM dogiadung;");
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String maHang = jTextField2.getText();
        String tenHang = jTextField3.getText();
        String hangSanXuat = jTextField4.getText();
        int donGia = Integer.parseInt(jTextField5.getText());

        String insertQuery = "INSERT INTO dogiadung (mahang, tenhang, hangsx, dongia) VALUES ('" + maHang + "', '" + tenHang + "', '" + hangSanXuat + "', " + donGia + ")";

        int rowCount = KetNoi.executeUpdate(insertQuery);
        if (rowCount > 0) {
            JOptionPane.showMessageDialog(this, "Thêm mặt hàng thành công.");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm mặt hàng.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

